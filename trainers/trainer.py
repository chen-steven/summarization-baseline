import logging
import torch
from torch.utils.data import DataLoader, RandomSampler
from transformers import Adafactor, AdamW, get_scheduler
from tqdm import tqdm
import os

logger = logging.getLogger(__name__)

class Trainer(object):
    def __init__(self,
                 args,
                 model,
                 train_dataset,
                 dev_dataset,
                 lr_scheduler=None,
                 optimizer=None
                 ):
        self.args = args
        self.model = model
        self.train_dataset = train_dataset
        self.dev_dataset = dev_dataset
        self.lr_scheduler = lr_scheduler
        self.optimizer = optimizer
        train_sampler = RandomSampler(self.train_dataset)
        self.train_dataloader = DataLoader(self.train_dataset, sampler=train_sampler, batch_size=self.args.batch_size)
        self.dev_dataloader = DataLoader(self.dev_dataset, shuffle=False, batch_size=self.args.batch_size)

    def compute_loss(self, inputs):
        raise NotImplementedError

    def training_step(self, inputs):
        self.model.train()
        loss = self.compute_loss(inputs)

        if self.args.gradient_accumulation_steps > 1:
            loss = loss / self.args.gradient_accumulation_steps

        loss.backward()
        return loss

    def train(self):
        max_steps = len(self.train_dataloader) * self.args.epochs
        self.create_optimizer_and_scheduler(num_training_steps=max_steps)

        logger.info("***** Running training *****")
        logger.info(f"  Num examples = {len(self.train_dataset)}")
        logger.info(f"  Num Epochs = {self.args.epochs}")
        logger.info(f"  Instantaneous batch size per device = {self.args.per_device_train_batch_size}")
        logger.info(f"  Total train batch size (w. parallel, distributed & accumulation) = {total_train_batch_size}")
        logger.info(f"  Gradient Accumulation steps = {self.args.gradient_accumulation_steps}")

        tr_loss = torch.tensor(0.0).to(self.args.device)
        self.model.zero_grad()

        for epoch in range(self.args.epochs):
            epoch_iterator = tqdm(self.train_dataloader)
            for step, inputs in enumerate(epoch_iterator):
                tr_loss += self.training_step(self.model, inputs)

                if (step + 1) % self.args.gradient_accumulation_steps == 0:
                    # Gradient clipping
                    if self.args.max_grad_norm is not None and self.args.max_grad_norm > 0:
                            torch.nn.utils.clip_grad_norm_(self.model.parameters(), self.args.max_grad_norm)

                    self.optimizer.step()
                    self.lr_scheduler.step()
                    self.model.zero_grad()
                    self.state.global_step += 1

            self._save()

    def create_optimizer_and_scheduler(self, num_training_steps: int):
        """
        Setup the optimizer and the learning rate scheduler.
        We provide a reasonable default that works well. If you want to use something else, you can pass a tuple in the
        Trainer's init through :obj:`optimizers`, or subclass and override this method in a subclass.
        """
        if self.optimizer is None:
            no_decay = ["bias", "LayerNorm.weight"]
            optimizer_grouped_parameters = [
                {
                    "params": [p for n, p in self.model.named_parameters() if not any(nd in n for nd in no_decay)],
                    "weight_decay": self.args.weight_decay,
                },
                {
                    "params": [p for n, p in self.model.named_parameters() if any(nd in n for nd in no_decay)],
                    "weight_decay": 0.0,
                },
            ]

            if self.args.adafactor:
                optimizer_cls = Adafactor
                optimizer_kwargs = {"scale_parameter": False, "relative_step": False}
            else:
                optimizer_cls = AdamW
                optimizer_kwargs = {
                    "betas": (self.args.adam_beta1, self.args.adam_beta2),
                    "eps": self.args.adam_epsilon,
                }
            optimizer_kwargs["lr"] = self.args.learning_rate

            self.optimizer = optimizer_cls(optimizer_grouped_parameters, **optimizer_kwargs)

        if self.lr_scheduler is None:
            self.lr_scheduler = get_scheduler(
                self.args.lr_scheduler_type,
                self.optimizer,
                num_warmup_steps=self.args.warmup_steps,
                num_training_steps=num_training_steps,

            )

